// CaseExpr represents a CASE expression.
type CaseExpr struct {
	Expr  Expr
	Whens []*When
	Else  Expr
}

// Format formats the node.
func (node *CaseExpr) Format(ctx Rewriter, buf *TrackedBuffer) {
	buf.Myprintf(ctx, "case ")
	if node.Expr != nil {
		buf.Myprintf(ctx, "%v ", node.Expr)
	}
	for _, when := range node.Whens {
		buf.Myprintf(ctx, "%v ", when)
	}
	if node.Else != nil {
		buf.Myprintf(ctx, "else %v ", node.Else)
	}
	buf.Myprintf(ctx, "end")
}

func (node *CaseExpr) walkSubtree(ctx interface{}, visit Visit) error {
	if node == nil {
		return nil
	}
	if err := Walk(ctx, visit, node.Expr); err != nil {
		return err
	}
	for _, n := range node.Whens {
		if err := Walk(ctx, visit, n); err != nil {
			return err
		}
	}
	return Walk(ctx, visit, node.Else)
}

func (node *CaseExpr) replace(from, to Expr) bool {
	for _, when := range node.Whens {
		if replaceExprs(from, to, &when.Cond, &when.Val) {
			return true
		}
	}
	return replaceExprs(from, to, &node.Expr, &node.Else)
}

