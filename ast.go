// Backtick produces a backticked literal given an input string.
func Backtick(in string) string {
	var buf bytes.Buffer
	buf.WriteByte('`')
	for _, c := range in {
		buf.WriteRune(c)
		if c == '`' {
			buf.WriteByte('`')
		}
	}
	buf.WriteByte('`')
	return buf.String()
}

func formatID(buf *TrackedBuffer, original, lowered string, requireQuotesForUppercase bool) {
	isDbSystemVariable := false
	if len(original) > 1 && original[:2] == "@@" {
		isDbSystemVariable = true
	}

	mustQuote := false
	for i, c := range original {
		if !isLetter(uint16(c)) && (!isDbSystemVariable || !isCarat(uint16(c))) {
			if i == 0 || !isDigit(uint16(c)) {
				mustQuote = true
				break
			}
		}
		if isCapitalLetter(uint16(c)) && requireQuotesForUppercase {
			mustQuote = true
			break
		}
	}
	if _, ok := keywords[lowered]; ok {
		mustQuote = true
	}

	if !mustQuote {
		_, _ = buf.WriteString(original)
		return
	}

	buf.WriteByte('"')
	for _, c := range original {
		buf.WriteRune(c)
		if c == '"' {
			buf.WriteByte('"')
		}
	}
	buf.WriteByte('"')
}

func compliantName(in string) string {
	var buf bytes.Buffer
	for i, c := range in {
		if !isLetter(uint16(c)) {
			if i == 0 || !isDigit(uint16(c)) {
				buf.WriteByte('_')
				continue
			}
		}
		buf.WriteRune(c)
	}
	return buf.String()
}
