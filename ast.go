// Show represents a show statement.
type Show struct {
	Type          string
	OnTable       TableName
	ShowTablesOpt *ShowTablesOpt
	Scope         string
}

// Format formats the node.
func (node *Show) Format(ctx Rewriter, buf *TrackedBuffer) {
	if node.Type == "tables" && node.ShowTablesOpt != nil {
		opt := node.ShowTablesOpt
		if opt.DbName != "" {
			if opt.Filter != nil {
				buf.Myprintf(ctx, "show %s%stables from %s %v", opt.Extended, opt.Full, opt.DbName, opt.Filter)
			} else {
				buf.Myprintf(ctx, "show %s%stables from %s", opt.Extended, opt.Full, opt.DbName)
			}
		} else {
			if opt.Filter != nil {
				buf.Myprintf(ctx, "show %s%stables %v", opt.Extended, opt.Full, opt.Filter)
			} else {
				buf.Myprintf(ctx, "show %s%stables", opt.Extended, opt.Full)
			}
		}
		return
	}
	if node.Scope == "" {
		buf.Myprintf(ctx, "show %s", node.Type)
	} else {
		buf.Myprintf(ctx, "show %s %s", node.Scope, node.Type)
	}
	if node.HasOnTable() {
		buf.Myprintf(ctx, " on %v", node.OnTable)
	}
}

